param
(
    [object]$EventInfo
)

$ErrorActionPreference = "Stop"

#Helpers
$helpersPath = Join-Path -Path $PSScriptRoot -ChildPath '..\helpers'
$config = Import-PowerShellDataFile "$PSScriptRoot\..\Config.psd1"
Import-Module "$helpersPath\helper.HealthFaults.psm1"
Import-Module "$helpersPath\helper.NetworkATC.psm1"

Import-LocalizedData -BindingVariable "LocMessage" -FileName "LocalizationMessages" -BaseDirectory $PSScriptRoot\..\..\

Add-FaultApi

$logName = 'NetworkHud'

$ATCAdapters = (Get-NetworkATCAdapters -ClusterName (Get-Cluster).Name)

$disconnects = @{}

foreach ($adapter in $ATCAdapters.Adapters)
{
    $disconnects[$adapter] = @()
}

$searchDisconnectHash = @{
    logname   = $logName
    id        = $($config.WatchLinkState.LinkStateChange.EventID)
    StartTime = (Get-Date).AddSeconds($config.MeasureLinkState.ClearIntervalSeconds * -1) #Subtract TimeSpan
}
$adapterDisconnects = Get-WinEvent -FilterHashtable $searchDisconnectHash -ErrorAction SilentlyContinue

foreach ($evt in $adapterDisconnects)
{
    foreach ($adapter in $ATCAdapters.Adapters)
    {
        $name = $adapter.name
        if ($evt.Message.Contains("[$name]"))
        {
            if ($null -eq $disconnects[$name])
            {
                $disconnects[$name] = @()
            }
            else
            {
                $disconnects[$name] = $disconnects[$name] + @($evt)
            }
            break
        }
    }
}

foreach ($adapter in $disconnects.Keys)
{
    $clearEvts = $disconnects[$adapter] | Sort-Object -Property TimeCreated -Descending
    Write-Host "Found $($faultEvts.Count) disconnect events within faulting interval $($config.WatchLinkState.FaultIntervalSeconds) seconds and $($clearEvts.Count) events within clear interval $($config.MeasureLinkState.ClearIntervalSeconds) seconds for $adapter"
    
    if ($clearEvts.Count -le $config.MeasureLinkState.ClearThreshold)
    {
        Write-Host "The disconnect events within clear interval for $adapter is less than $($config.MeasureLinkState.ClearThreshold)."
        $ResourceID =  "$($env:COMPUTERNAME): $($Adapter.Name)"
        [Microsoft.NetworkHud.Module.HciHealthUtils]::HciModifyFault($config.WatchLinkState.EntityType, $ResourceID, "", "", "", $FRApiActionDelete, $config.WatchLinkState.FaultType, $HealthUrgencyUnHealthy, "", "", "", 0)
    }
}

