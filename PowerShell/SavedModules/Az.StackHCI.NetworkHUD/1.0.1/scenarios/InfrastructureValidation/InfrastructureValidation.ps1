param
(
    [object]$EventInfo
)

$ErrorActionPreference = "Stop"

$config = (Import-PowerShellDataFile "$PSScriptRoot\..\Config.psd1")
Import-LocalizedData -BindingVariable "LocMessage" -FileName "LocalizationMessages" -BaseDirectory $PSScriptRoot\..\..\
$logName = 'NetworkHud'
$helpersPath = Join-Path -Path $PSScriptRoot -ChildPath '..\helpers'
Import-Module "$helpersPath\helper.HealthFaults.psm1"
Import-Module "$helpersPath\helper.NetworkATC.psm1"
Add-FaultApi

$MissingIntentTypes = Get-IntentTypesUsage -Missing

if ($MissingIntentTypes) 
{
    Write-EventLog  -LogName $logName `
    -Source    $($config.InfrastructureValidation.MissingIntentTypes.Source) `
    -EventID   $($config.InfrastructureValidation.MissingIntentTypes.EventID) `
    -EntryType $($config.InfrastructureValidation.MissingIntentTypes.EntryType) `
    -Message $($LocMessage.$($config.InfrastructureValidation.MissingIntentTypes.MessageKey) -f $($MissingIntentTypes -join ', '))

    $faultDescription = $LocMessage.$($config.InfrastructureValidation.MissingIntentTypesFaultDescriptionKey) -f $($MissingIntentTypes -join ', ')
    $faultAction = $LocMessage.$($config.InfrastructureValidation.MissingIntentTypesFaultActionKey) -f $($MissingIntentTypes -join ', ')
    $ResourceID = (Get-Cluster).Name

    [Microsoft.NetworkHud.Module.HciHealthUtils]::HciModifyFault($config.InfrastructureValidation.EntityType, $ResourceID, "", "", "", $FRApiActionModify, $config.InfrastructureValidation.FaultType, $HealthUrgencyWarning, "", $faultDescription, $faultAction, 0)
    [Microsoft.NetworkHud.Module.HciHealthUtils]::HciModifyRelationship($config.InfrastructureValidation.EntityType, $ResourceID, "", "", "", $FRApiActionModify, "Microsoft.Health.EntityType.Server", $env:COMPUTERNAME, "", "", "", $HealthRelationshipGroupKey, $HealthUrgencyWarning, $HealthRelationshipCollection, 0)
}
else
{
    $ResourceID = (Get-Cluster).Name
    [Microsoft.NetworkHud.Module.HciHealthUtils]::HciModifyFault($config.InfrastructureValidation.EntityType,  $ResourceID, "", "", "", $FRApiActionDelete, $config.InfrastructureValidation.FaultType, $HealthUrgencyUnHealthy, "", "", "", 0)
}
